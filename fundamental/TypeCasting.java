package fundamental;

public class TypeCasting {
    public static void main(String[] args){
        int a=7; //lower value data
        float b=a;//higher value data
        System.out.println("float "+b);
        
        float c=71.0f;//higher value data
        int e= (int) c;//lower value data
        System.out.println("Int "+e);
        System.out.println(Math.max(45, Math.max(45, 4948)));
        
    }
    
}
